        bool isObstacle =
            gridData[row][col] == 1 && col > 0 && col < 31 && row > 11 && row < 70;

        Border borda = pintor.pintaBorda(
            isObstacle, isTopo, isBase, isEsquerda, isDireita);
        if (bordas.length < num) {
          bordas.add(borda);
          cores.add(color);
          texto.add("");
